var x = 1;
while (x <= 10) {
  console.log(x);
  x++;
}

for (y = 1; y <= 10; y++) {
  console.log(y);
}

var z = 1;
do {
  console.log(z);
  z++;
} while (z <= 10);

// do:                   while
// 1 variable            1 variable
// 2 actions             2 condition
// 3 condition           3 actions
